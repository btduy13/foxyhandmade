const fs = require("fs");
const path = require("path");

const ABOUT_DIR = path.join(__dirname, "../../public/images/about");
const OUTPUT_FILE = path.join(__dirname, "../data/about-images.js");

function scanDirectory() {
  if (!fs.existsSync(ABOUT_DIR)) {
    console.error(`Directory not found: ${ABOUT_DIR}`);
    process.exit(1);
  }

  const files = fs.readdirSync(ABOUT_DIR);
  const products = [];
  const menu = [];

  files.forEach((file) => {
    const fullPath = path.join(ABOUT_DIR, file);
    const stat = fs.statSync(fullPath);

    if (stat.isDirectory()) {
      if (file === "menu") {
        const menuFiles = fs.readdirSync(fullPath);
        menuFiles.forEach((menuFile) => {
          if (
            menuFile.endsWith(".jpg") ||
            menuFile.endsWith(".png") ||
            menuFile.endsWith(".jpeg")
          ) {
            menu.push(`/images/about/menu/${menuFile}`);
          }
        });
      }
    } else {
      if (
        file.endsWith(".jpg") ||
        file.endsWith(".png") ||
        file.endsWith(".jpeg")
      ) {
        products.push(`/images/about/${file}`);
      }
    }
  });

  const content = `// This file is auto-generated by src/scripts/generate-about-images.js. Do not edit manually.
export const aboutImages = {
  products: ${JSON.stringify(products, null, 2)},
  menu: ${JSON.stringify(menu, null, 2)}
};
`;

  fs.writeFileSync(OUTPUT_FILE, content, "utf8");
  console.log(`Successfully generated ${OUTPUT_FILE}`);
  console.log(`Found ${products.length} product images and ${menu.length} menu images.`);
}

scanDirectory();
